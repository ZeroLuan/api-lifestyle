package br.com.sysmap.backend.security;

import br.com.sysmap.backend.entity.Users;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;

/**
 * Wrapper que adapta a entidade Users para o contrato UserDetails do Spring Security.
 * Não implementa Roles pois o sistema não exige controle de permissões por perfil.
 */
public class AuthenticatedUser implements UserDetails {

    private final Users user;

    public AuthenticatedUser(Users user) {
        this.user = user;
    }

    public Users getUser() {
        return user;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return List.of();
    }

    @Override
    public String getPassword() {
        return user.getPassword();
    }

    @Override
    public String getUsername() {
        return user.getEmail();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return user.getDeletedAt() == null;
    }
}
